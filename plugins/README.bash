bash.pl - Quote plugin for mootykins3
=====================================

A plugin which gives quotes from bash.org, qdb.us, german-bash.org and GroupHug.us (!bash, !qdb, !gbo, !gh, give a number to get a specific quote).

ATTENTION: In order to use this plugin, you need to install the LWP::UserAgent module. (For example, by doing sudo perl -MCPAN -e "install 'LWP::UserAgent'" on the command line - if some of the network tests fail, don't enable them.)
